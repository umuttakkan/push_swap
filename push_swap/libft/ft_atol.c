#include "libft.h"

int	ft_atoi(const char *str)
{
	int		u;
	long	m;
	int		t;

	u = 0;
	m = 0;
	t = 1;
	while ((str[u] == 32) || (str[u] >= 9 && str[u] <= 13))
		u++;
	if (str[u] == '-' || str[u] == '+')
	{
		if (str[u] == '-')
			t *= -1;
		u++;
	}
	while (str[u] >= '0' && str[u] <= '9')
	{
		m = (str[u] - '0') + (m * 10);
		u++;
	}
	return (t * m);
}